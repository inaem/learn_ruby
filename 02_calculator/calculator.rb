def add(number_one, number_two)
  number_one + number_two
end
def subtract(number_one, number_two)
  number_one - number_two
end
def sum(array)
  sum = 0
  array.each do |number|
    sum = sum + number
  end
  sum
end
def multiply(number_one,number_two)
  number_one * number_two
end
def power(number_one, number_two)
  number_one ** number_two
end
